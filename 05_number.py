# 정수형

print(4 + 7)
# 11
print(2 - 4)
# -2
print(5 * 3)
# 15
print(7 % 3)
# 1
print(2 ** 3)
# 8


# 소수형

print(4.0 + 7.0)
# 11.0
print(2.0 - 4)
# -2.0
print(7 / 2)
# 3.5
print(6 / 2)
# 3.0
print(7.0 / 2)
# 3.5
print(6.0 / 2.0)
# 3.0


# 연산순서

print(2 + 3 * 3)
# 11
print(2 * (2 + 3))
# 10


# 버림 나눗셈

print(7 // 2)
# 3
print(8 // 3)
# 2
print(8.0 // 3)
# 2.0
print(8 // 3.0)
# 2.0


# round(반올림)

print(round(3.1415926535))
# 3
print(round(3.1415926535, 2))
# 3.14
print(round(3.1415926535, 4))
# 3.1416